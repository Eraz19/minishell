#include <stdlib.h>
#include "../../../_lexer.h"

t_token	*eof(size_t offset)
{
	t_token	*res;

	res = malloc(sizeof(t_token));
	if (res == NULL)
		return (NULL);
	res->offset = offset;
	res->type = EOF;
	if (buff_init(&res->value, 0) == false)
		return (free(res), NULL);
	return (res);
}
